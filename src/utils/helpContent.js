// 경로에서 도움말 콘텐츠 가져오기
export const getHelpContent = (pathname) => {
  // 정확한 경로 매칭
  if (PAGE_HELP[pathname]) return PAGE_HELP[pathname];

  // 부분 경로 매칭 (예: /music-room/xxx)
  const matchedKey = Object.keys(PAGE_HELP).find((key) =>
    pathname.startsWith(key),
  );
  return matchedKey ? PAGE_HELP[matchedKey] : null;
};
